{
	//WIRE_WRITE(uint8_t *data);
	 if(BufferLength >= BUFFER_LENGTH){
		 return 0;
	 }
	 // put byte in tx buffer
	 Buffer[BufferIndex] = data;
	 ++BufferIndex;
	 // update amount in buffer
	 BufferLength = BufferIndex;
	 return 1;
}

